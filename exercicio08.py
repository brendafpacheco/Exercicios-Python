# Exercicio 008
# Faca um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salario no referido mês.

salarioHora = float(input('Seu salario por hora de trabalho: '))
horasTrabalhadasMes = float(input('Quantas horas foram trabalhadas neste mês: '))
salarioMes = salarioHora * horasTrabalhadasMes
print(salarioMes)